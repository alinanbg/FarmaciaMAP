    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed
        // TODO add your handling code here:
        
        try{
            if (farmaciaSelecionada != null){
                 int op = JOptionPane.showConfirmDialog(this,
                         "excluir titulo"
                         //MensagensUtil.getMensagem(mensagensUtil.MSG_LABEL_EXCLUIR_TITULO)
                            +farmaciaSelecionada.getNome() + "?",
                         "excluir", 
                        // MensagensUtil.getMensagem(mensagensUtil.MSG_LABEL_EXCLUIR),
                        
                        JOptionPane.OK_CANCEL_OPTION);
                 
                 if(op == JOptionPane.OK_OPTION){
                     farmaciaDAO.excluir(farmaciaSelecionada);
                     farmaciaSelecionada = null;
                     preencherTabela(null);
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "erro ao excluir"
                    //MensagensUtil.getMensagem(mensagensUtil.MSG_ERRO_EXCLUIR )
            );
        }
        
    }//GEN-LAST:event_buttonExcluirActionPerformed

    private void tableFarmaciasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableFarmaciasMouseClicked
        // TODO add your handling code here:
        try{ 
            JTable tabelaFarmacias = (JTable) evt.getSource();
            int linha = tabelaFarmacias.rowAtPoint(evt.getPoint());
            int codigo = (int) tabelaFarmacias.getModel().getValueAt(linha, 0);
        
            farmaciaSelecionada = farmaciaDAO.buscarPorCodigo(codigo);
        }catch (Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "erro");
            //JOptionPane.showMessageDialog(this, MensagensUtil.getMensagem(MensagensUtil.MSG_ERRO_CLIQUE));
        }
        
    }//GEN-LAST:event_tableFarmaciasMouseClicked
    
    public void preencherTabela(List<Farmacia> lista) {
        try {

            List<Farmacia> farmacias = null;

            if (lista == null) {
                farmacias = farmaciaDAO.listar();
            } else {
                farmacias = lista;
            }

            DefaultTableModel model = new DefaultTableModel();

            model.setColumnCount(11);
            model.setColumnIdentifiers(
                    new String[]{"Código", "Nome", "CNPJ", "Telefone", "E-mail",
                        "Cidade", "Estado", "CEP", "Rua", "N°", "Bairro"});
            
            
            model.setRowCount(farmacias.size());

            for (int i = 0; i < farmacias.size(); i++) {

                Farmacia f = farmacias.get(i);

                model.setValueAt(f.getCodigo(), i, 0);
                model.setValueAt(f.getNome(), i, 1);
                model.setValueAt(f.getCnpj(), i, 2);
                model.setValueAt(f.getTelefone(), i, 3);
                model.setValueAt(f.getEmail(), i, 4);
                model.setValueAt(f.getId_endereco().getCidade(), i, 5);
                model.setValueAt(f.getId_endereco().getEstado(), i, 6);
                model.setValueAt(f.getId_endereco().getCep(), i, 7);
                model.setValueAt(f.getId_endereco().getRua(), i, 8);
                model.setValueAt(f.getId_endereco().getNumero(), i, 9);
                model.setValueAt(f.getId_endereco().getBairro(), i, 10);
                
            }

            tableFarmacias.setModel(model);

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage());
        }
    }
    
