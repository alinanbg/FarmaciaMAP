    private void buttonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditarActionPerformed
        // TODO add your handling code here:
        
        try{
            if(farmaciaSelecionada != null){
                String nome = JOptionPane.showInputDialog(this,"editar nome");
                        //MensagensUtil.getMensagem(mensagensUtil.MSG_LABEL_EDITAR_NOME));
                String cnpj = JOptionPane.showInputDialog(this,"editar cnpj");
                
                //adicionar todas as opções de editar
                
                Farmacia f = new Farmacia(nome, cnpj);
                f.setCodigo(farmaciaSelecionada.getCodigo());
                
                farmaciaDAO.editar(f);
                
                preencherTabela(null);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "erro ao editar"
                    //MensagensUtil.getMensagem(mensagensUtil.MSG_ERRO_EDITAR)
                    );
        }
        
    }//GEN-LAST:event_buttonEditarActionPerformed

